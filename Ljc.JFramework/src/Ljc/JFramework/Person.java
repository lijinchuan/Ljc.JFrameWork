package Ljc.JFramework;

public class Person {
	private String _name = null;
	private int _age = 0;

	public String getName() {
		return this._name;
	}

	public void setName(String val) {
		this._name = val;
	}

	public int getAge() {
		return this._age;
	}

	public void setAge(int val) {
		this._age = val;
	}
}
